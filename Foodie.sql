-- Delete duplicate data
DELETE FROM foodie_raw
WHERE
    ROWID IN (
        SELECT
            rid
        FROM
            (
                SELECT
                    ROWID AS rid, ROW_NUMBER()
                                  OVER(PARTITION BY state, city, restaurant_name, order_date, location,
                                                    category, rating, rating_count, dish_name, price_npr
                                       ORDER BY
                                           ROWID
                    )     AS rn
                FROM
                    foodie_raw
            )
        WHERE
            rn > 1
    );

--Creating Schema
--Dimension tables

CREATE TABLE dim_date (
    date_id     NUMBER
        GENERATED BY DEFAULT AS IDENTITY
    PRIMARY KEY,
    full_date   DATE,
    year_num    NUMBER,
    month_num   NUMBER,
    month_name  VARCHAR2(20),
    quarter_num NUMBER,
    day_num     NUMBER,
    week_num    NUMBER
);

CREATE TABLE dim_location (
    location_id NUMBER
        GENERATED BY DEFAULT AS IDENTITY
    PRIMARY KEY,
    state       VARCHAR2(100),
    city        VARCHAR2(100),
    location    VARCHAR2(200)
);

CREATE TABLE dim_restaurant (
    restaurant_id   NUMBER
        GENERATED BY DEFAULT AS IDENTITY
    PRIMARY KEY,
    restaurant_name VARCHAR2(200)
);

CREATE TABLE dim_category (
    category_id NUMBER
        GENERATED BY DEFAULT AS IDENTITY
    PRIMARY KEY,
    category    VARCHAR2(200)
);

CREATE TABLE dim_dish (
    dish_id   NUMBER
        GENERATED BY DEFAULT AS IDENTITY
    PRIMARY KEY,
    dish_name VARCHAR2(200)
);

--Fact Table
CREATE TABLE fact_foodie_orders (
    order_id      NUMBER
        GENERATED BY DEFAULT AS IDENTITY
    PRIMARY KEY,
    date_id       NUMBER,
    price_npr     NUMBER(10, 2),
    rating        NUMBER(4, 2),
    rating_count  NUMBER,
    location_id   NUMBER,
    restaurant_id NUMBER,
    category_id   NUMBER,
    dish_id       NUMBER,
    CONSTRAINT fk_date FOREIGN KEY ( date_id )
        REFERENCES dim_date ( date_id ),
    CONSTRAINT fk_location FOREIGN KEY ( location_id )
        REFERENCES dim_location ( location_id ),
    CONSTRAINT fk_restaurant FOREIGN KEY ( restaurant_id )
        REFERENCES dim_restaurant ( restaurant_id ),
    CONSTRAINT fk_category FOREIGN KEY ( category_id )
        REFERENCES dim_category ( category_id ),
    CONSTRAINT fk_dish FOREIGN KEY ( dish_id )
        REFERENCES dim_dish ( dish_id )
);

--Data insertion
INSERT INTO dim_date (
    full_date,
    year_num,
    month_num,
    month_name,
    quarter_num,
    day_num,
    week_num
)
    SELECT DISTINCT
        order_date,
        EXTRACT(YEAR FROM order_date),
        EXTRACT(MONTH FROM order_date),
        rtrim(to_char(order_date, 'Month')),
        TO_NUMBER(to_char(order_date, 'Q')),
        EXTRACT(DAY FROM order_date),
        TO_NUMBER(to_char(order_date, 'IW'))
    FROM
        foodie_raw
    WHERE
        order_date IS NOT NULL;

INSERT INTO dim_location (
    state,
    city,
    location
)
    SELECT DISTINCT
        state,
        city,
        location
    FROM
        foodie_raw
    WHERE
        state IS NOT NULL
        AND city IS NOT NULL
        AND location IS NOT NULL;

INSERT INTO dim_restaurant ( restaurant_name )
    SELECT DISTINCT
        restaurant_name
    FROM
        foodie_raw
    WHERE
        restaurant_name IS NOT NULL;

INSERT INTO dim_category ( category )
    SELECT DISTINCT
        category
    FROM
        foodie_raw
    WHERE
        category IS NOT NULL;

INSERT INTO dim_dish ( dish_name )
    SELECT DISTINCT
        dish_name
    FROM
        foodie_raw
    WHERE
        dish_name IS NOT NULL;

INSERT INTO fact_foodie_orders (
    date_id,
    price_npr,
    rating,
    rating_count,
    location_id,
    restaurant_id,
    category_id,
    dish_id
)
    SELECT
        dd.date_id,
        f.price_npr,
        f.rating,
        f.rating_count,
        dl.location_id,
        dr.restaurant_id,
        dc.category_id,
        dsh.dish_id
    FROM
             foodie_raw f
        JOIN dim_date       dd ON dd.full_date = f.order_date
        JOIN dim_location   dl ON dl.state = f.state
                                AND dl.city = f.city
                                AND dl.location = f.location
        JOIN dim_restaurant dr ON dr.restaurant_name = f.restaurant_name
        JOIN dim_category   dc ON dc.category = f.category
        JOIN dim_dish       dsh ON dsh.dish_name = f.dish_name;

